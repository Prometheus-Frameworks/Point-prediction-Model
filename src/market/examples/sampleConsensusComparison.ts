const buildEdge = (
  projection: MarketProjectionInput,
  consensus: ConsensusInput,
): MarketEdgeOutput => {
  const comparison = compareToConsensus(projection, consensus);
  const trust = scoreTrustAdjustedEdge(comparison, projection);
  const edge: MarketEdgeOutput = {
    ...comparison,
    ...trust,
    flags: [],
    explanation: [],
  };

  edge.flags = buildEdgeFlags(edge, projection);
  edge.explanation = buildEdgeExplanation(edge, projection);

  return edge;
};

